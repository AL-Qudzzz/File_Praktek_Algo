public class ArrayNumber {
    public static void main(String[] args) {
        int[][] myNumbers = {
                {1, 2, 3, 4},       // myNumbers[0]
                {5, 6, 7},          // myNumbers[1]
                {8, 9, 10, 11},     // myNumbers[2]
                {12, 13}            // myNumbers[3]
        };
        System.out.println(myNumbers[3][0]);
    }
}
